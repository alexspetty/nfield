from math import gcd

def is_prime(n):
    if n < 2: return False
    if n < 4: return True
    if n % 2 == 0 or n % 3 == 0: return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0: return False
        i += 6
    return True

def mult_order(g, p):
    r = g % p
    if r == 0: return 0
    o, pw = 1, r
    while pw != 1:
        pw = (pw * g) % p
        o += 1
    return o

def collision_count(g, p, b):
    count = 0
    for r in range(1, p):
        d_r = b * r // p
        gr = (g * r) % p
        d_gr = b * gr // p
        if d_r == d_gr:
            count += 1
    return count

def row_sum(g, p):
    """Sum of C_b(g) over all bases b = 2, ..., p-1."""
    return sum(collision_count(g, p, b) for b in range(2, p))

def N_count(r, s, p):
    """Number of bases b in {2,...,p-1} where r and s are in the same bin."""
    count = 0
    for b in range(2, p):
        if b * r // p == b * s // p:
            count += 1
    return count

# Direct formula attempt: R(g) via pairwise distances
def row_sum_via_distances(g, p):
    """R(g) = sum_r N(r, gr mod p)."""
    total = 0
    for r in range(1, p):
        s = (g * r) % p
        total += N_count(r, s, p)
    return total

