#!/usr/bin/env python3
"""
Sign structure for ALL bases (prime and composite).
Uses direct DFT on the unit group without requiring primitive roots.
"""

from math import gcd, pi, cos, sin
from collections import defaultdict

def is_prime(n):
    if n < 2: return False
    if n < 4: return True
    if n % 2 == 0 or n % 3 == 0: return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0: return False
        i += 6
    return True

def group_structure(m):
    """
    Compute generators and orders for (Z/mZ)*.
    Returns list of (generator, order) pairs such that the group
    is the direct product of cyclic groups <g_i> of order o_i.
    """
    units = sorted(a for a in range(1, m) if gcd(a, m) == 1)
    phi = len(units)
    if phi == 0:
        return [], []

    # Find group structure by iterative quotient
    generators = []
    orders = []
    remaining = set(units)

    while len(remaining) > 1:
        # Find element of largest order
        best_g, best_ord = 1, 1
        for g in sorted(remaining):
            if g == 1: continue
            val, ord = g, 1
            while val != 1:
                val = val * g % m
                ord += 1
            if ord > best_ord:
                best_g, best_ord = g, ord

        if best_ord == 1:
            break

        generators.append(best_g)
        orders.append(best_ord)

        # Remove the subgroup generated by best_g from consideration
        # by reducing: quotient out <best_g>
        subgroup = set()
        val = 1
        for _ in range(best_ord):
            subgroup.add(val)
            val = val * best_g % m

        # Keep one representative per coset
        new_remaining = set()
        seen_cosets = set()
        for a in sorted(remaining):
            coset = frozenset(a * s % m for s in subgroup)
            if coset not in seen_cosets:
                seen_cosets.add(coset)
                new_remaining.add(a)
        remaining = new_remaining

    return generators, orders

def build_all_characters(m):
    """Build all Dirichlet characters mod m using group structure."""
    units = sorted(a for a in range(1, m) if gcd(a, m) == 1)
    phi = len(units)
    if phi == 0:
        return []

    generators, orders = group_structure(m)

    # Build discrete log: for each unit a, find (k1, k2, ...) such that
    # a = g1^k1 * g2^k2 * ... mod m
    # This is the coordinate in the product of cyclic groups.

    # Build lookup: enumerate all products
    from itertools import product as iterproduct

    coords = {}
    ranges = [range(o) for o in orders]
    for ks in iterproduct(*ranges):
        val = 1
        for g, k in zip(generators, ks):
            val = val * pow(g, k, m) % m
        if val in coords and coords[val] != ks:
            pass  # multiple representations, shouldn't happen
        coords[val] = ks

    # Characters: chi_{j1,...,jn}(a) = prod exp(2*pi*i*j_k*coord_k/order_k)
    chars = []
    j_ranges = [range(o) for o in orders]
    for js in iterproduct(*j_ranges):
        chi = {}
        for a in range(m):
            if gcd(a, m) != 1:
                chi[a] = 0
            else:
                ks = coords.get(a)
                if ks is None:
                    chi[a] = 0
                    continue
                angle = sum(2 * pi * j * k / o
                           for j, k, o in zip(js, ks, orders))
                chi[a] = complex(cos(angle), sin(angle))

        val_minus1 = chi.get(m - 1, 0)
        is_odd = abs(val_minus1 - (-1)) < 1e-6

        chars.append({'js': js, 'chi': chi, 'is_odd': is_odd})

    return chars

def run_base(b, max_prime=2000000):
    m = b * b
    units = sorted(a for a in range(1, m) if gcd(a, m) == 1)
    phi = len(units)

    # Build S table
    S_table = {}
    for p in range(m + 1, min(80000, max_prime)):
        if not is_prime(p) or gcd(p, b) != 1:
            continue
        a = p % m
        if a in S_table:
            continue
        C = sum(1 for r in range(1, p)
                if b * r // p == b * (b * r % p) // p)
        S_table[a] = C - (p - 1) // b

    # Class means for centering
    cls = defaultdict(list)
    for a in units:
        if a in S_table:
            cls[(a - 1) % b].append(S_table[a])
    mean_R = {R: sum(v)/len(v) for R, v in cls.items()}

    S_cent = {}
    for a in units:
        if a in S_table:
            S_cent[a] = S_table[a] - mean_R[(a - 1) % b]

    # Build characters
    chars = build_all_characters(m)
    odd_chars = [c for c in chars if c['is_odd']]

    if len(odd_chars) == 0:
        return None

    # Compute s_hat
    for c in odd_chars:
        chi = c['chi']
        s_hat = sum(S_cent.get(a, 0) * chi.get(a, 0).conjugate()
                    for a in units) / phi
        c['s_hat'] = complex(s_hat)

    # Compute P(s, chi) at s = 0.5
    for c in odd_chars:
        c['P'] = complex(0)

    count = 0
    for p in range(m + 1, max_prime):
        if not is_prime(p) or gcd(p, b) != 1:
            continue
        count += 1
        a = p % m
        for c in odd_chars:
            chi_p = c['chi'].get(a, 0)
            if chi_p == 0: continue
            c['P'] += chi_p * p ** (-0.5)

    pos = sum(1 for c in odd_chars
              if (c['s_hat'] * c['P']).real >= -1e-10)
    neg = len(odd_chars) - pos
    net = sum((c['s_hat'] * c['P']).real for c in odd_chars)

    return {
        'b': b, 'm': m, 'phi': phi,
        'n_odd': len(odd_chars),
        'primes': count,
        'pos': pos, 'neg': neg, 'net': net,
        'is_prime': is_prime(b)
    }

print(f"{'base':>5} {'type':>6} {'phi':>4} {'odd':>4} "
      f"{'pos':>4} {'neg':>4} {'%pos':>6} {'net':>8}")

for b in range(3, 26):
    r = run_base(b)
    if r is None:
        continue
    pct = 100 * r['pos'] / r['n_odd'] if r['n_odd'] > 0 else 0
    btype = 'prime' if r['is_prime'] else 'comp'
    print(f"{r['b']:>5} {btype:>6} {r['phi']:>4} {r['n_odd']:>4} "
          f"{r['pos']:>4} {r['neg']:>4} {pct:>5.1f}% {r['net']:>+8.3f}")
