#!/usr/bin/env python3
"""
σ₀(3, 1) = 1/2: the explicit structure.

Base 3, mod 9. Six characters. Only j=1 and j=5 (conjugate pair)
contribute to F°. The entire question reduces to:

Does P(1/2, χ₁) converge, where χ₁ is the primitive character
of order 6 mod 9?

This is GRH for a single L-function: L(s, χ₁) mod 9.

Let's compute everything explicitly and see if the collision
transform structure gives us any leverage.
"""

import cmath
from math import gcd, log

def is_prime(n):
    if n < 2: return False
    if n < 4: return True
    if n % 2 == 0 or n % 3 == 0: return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0: return False
        i += 6
    return True

# The group (Z/9Z)* = {1,2,4,5,7,8}, generated by 2
# dlog: 1→0, 2→1, 4→2, 8→3, 7→4, 5→5
gen = 2
m = 9
dlog = {}
pw = 1
for k in range(6):
    dlog[pw] = k
    pw = (pw * gen) % m

omega = cmath.exp(2j * cmath.pi / 6)

print("="*60)
print("THE BASE-3 GRH PROBLEM")
print("="*60)

# S values
S = {1: 0, 2: 1, 4: 0, 5: -1, 7: -2, 8: -1}

# Class means
cls_mean = {0: -2/3, 1: -1/3}  # R=0: -2/3, R=1: -1/3

# Centered values
S_cent = {}
for a in dlog:
    R = (a - 1) % 3
    S_cent[a] = S[a] - cls_mean[R]

print("\nS° values (the collision stamp, centered):")
for a in sorted(dlog.keys()):
    print(f"  S°({a}) = {S_cent[a]:+.4f}")

# Characters
print("\nCharacters mod 9:")
print(f"  {'a':>3}  {'dlog':>5}", end="")
for j in range(6):
    print(f"  {'χ'+str(j):>8}", end="")
print()

for a in sorted(dlog.keys()):
    print(f"  {a:>3}  {dlog[a]:>5}", end="")
    for j in range(6):
        chi_val = omega ** (j * dlog[a])
        if abs(chi_val.imag) < 0.001:
            print(f"  {chi_val.real:>+8.3f}", end="")
        else:
            print(f"  {chi_val.real:>+4.1f}{chi_val.imag:>+4.1f}i", end="")
    print()

# Collision transform coefficients
print("\nCollision transform coefficients:")
for j in range(6):
    s_hat = 0
    for a in dlog:
        chi_bar = omega ** (-j * dlog[a])
        s_hat += S_cent[a] * chi_bar
    s_hat /= 6

    chi_neg1 = omega ** (j * 3)
    parity = "odd" if chi_neg1.real < -0.5 else "even"

    print(f"  ŝ(χ_{j}) = {s_hat.real:>+8.5f} {s_hat.imag:>+8.5f}i  "
          f"|ŝ| = {abs(s_hat):.5f}  {parity}")

print("\n" + "="*60)
print("F°(s) = 2·Re(ŝ(χ₁)·P(s,χ₁))")
print("The ENTIRE question is: does P(1/2, χ₁) converge?")
print("="*60)

# Compute P(s, χ₁) at multiple s values with increasing prime counts
max_p = 200000

# Track P(s, chi_1) at s = 1.0, 0.75, 0.5
s_values = [1.0, 0.75, 0.5]
checkpoints = [1000, 2000, 5000, 10000, 20000, 50000, 100000, 150000]

P = {s: 0+0j for s in s_values}
count = 0

print(f"\nP(s, χ₁) partial sums (primes to {max_p}):")
print(f"{'primes':>8} {'p_max':>8}", end="")
for s in s_values:
    print(f"  {'Re P('+str(s)+')':>12} {'|P|':>8}", end="")
print()

ci = 0
for p in range(5, max_p):
    if not is_prime(p) or p % 3 == 0:
        continue
    count += 1
    a = p % m
    dl = dlog.get(a)
    if dl is None:
        continue

    chi_1 = omega ** dl  # chi_1(p)

    for s in s_values:
        P[s] += chi_1 / (p ** s)

    if ci < len(checkpoints) and count >= checkpoints[ci]:
        print(f"{count:>8} {p:>8}", end="")
        for s in s_values:
            print(f"  {P[s].real:>+12.6f} {abs(P[s]):>8.4f}", end="")
        print()
        ci += 1

# Final summary
print(f"\n{'='*60}")
print(f"FINAL at {count} primes:")
for s in s_values:
    print(f"  P({s}, χ₁) = {P[s].real:>+10.6f} {P[s].imag:>+10.6f}i  |P| = {abs(P[s]):.6f}")

print(f"\nF°(s) = 2·Re(ŝ(χ₁)·P(s,χ₁)):")
s_hat_1 = 0
for a in dlog:
    s_hat_1 += S_cent[a] * omega**(-dlog[a])
s_hat_1 /= 6

for s in s_values:
    F = 2 * (s_hat_1 * P[s]).real
    print(f"  F°({s}) = {F:>+10.6f}")

# The L-function connection
print(f"\nThe L-function:")
print(f"  χ₁ is primitive of order 6 mod 9, conductor 9")
print(f"  χ₁(-1) = ω³ = -1 (odd character)")
print(f"  L(s, χ₁) is an odd primitive Dirichlet L-function mod 9")
print(f"\n  P(s, χ₁) converges at s = 1/2")
print(f"  ⟺ L(s, χ₁) has no zeros for Re(s) > 1/2")
print(f"  ⟺ GRH for this specific L-function")
print(f"\n  Computational verdict at {count} primes:")
print(f"  P(1/2, χ₁) = {P[0.5].real:>+.6f} {P[0.5].imag:>+.6f}i")
print(f"  Appears {'CONVERGENT' if abs(P[0.5]) < 2 else 'DIVERGENT'}")
