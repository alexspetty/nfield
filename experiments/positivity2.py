#!/usr/bin/env python3
"""
Compute collision transform coefficients and check positivity.
Uses CRT decomposition: (Z/100Z)* = (Z/4Z)* x (Z/25Z)*
"""

from math import gcd, pi, cos, sin, atan2
from collections import defaultdict

def is_prime(n):
    if n < 2: return False
    if n < 4: return True
    if n % 2 == 0 or n % 3 == 0: return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0: return False
        i += 6
    return True

def build_characters_mod_n(n):
    """Build all Dirichlet characters mod n via CRT."""
    # Factor n
    factors = []
    temp = n
    for p in range(2, n + 1):
        if temp == 1: break
        if temp % p == 0:
            pk = 1
            while temp % p == 0:
                pk *= p
                temp //= p
            factors.append(pk)

    if len(factors) == 1:
        return build_chars_prime_power(factors[0])

    # CRT: build chars for each factor, combine
    char_lists = [build_chars_prime_power(pk) for pk in factors]

    # Combine via CRT
    combined = []
    # Start with first factor's chars
    current = char_lists[0]
    current_mod = factors[0]

    for i in range(1, len(factors)):
        next_chars = char_lists[i]
        next_mod = factors[i]
        new_combined = []

        for c1 in current:
            for c2 in next_chars:
                # Combine: chi(a) = c1(a mod current_mod) * c2(a mod next_mod)
                new_mod = current_mod * next_mod
                chi = {}
                for a in range(new_mod):
                    if gcd(a, new_mod) != 1:
                        chi[a] = 0
                    else:
                        v1 = c1.get(a % current_mod, 0)
                        v2 = c2.get(a % next_mod, 0)
                        chi[a] = v1 * v2
                new_combined.append(chi)

        current = new_combined
        current_mod = current_mod * next_mod

    return current

def build_chars_prime_power(pk):
    """Build characters mod p^k using primitive root."""
    units = [a for a in range(pk) if gcd(a, pk) == 1]
    phi = len(units)

    # Find primitive root
    g = None
    for candidate in range(2, pk):
        if gcd(candidate, pk) != 1: continue
        order = 1
        val = candidate
        while val % pk != 1:
            val = val * candidate % pk
            order += 1
        if order == phi:
            g = candidate
            break

    if g is None:
        # pk = 2^k with k >= 3: no primitive root
        # Handle Z/2^k Z* = {-1} x <5> for k >= 3
        if pk == 1:
            return [{}]
        if pk == 2:
            return [{0: 0, 1: 1}]
        if pk == 4:
            # Z/4Z* = {1, 3}, generated by 3
            chars = []
            for j in range(2):
                chi = {}
                for a in range(4):
                    if gcd(a, 4) != 1:
                        chi[a] = 0
                    else:
                        # 3^0 = 1, 3^1 = 3
                        k = 0 if a == 1 else 1
                        angle = 2 * pi * j * k / 2
                        chi[a] = complex(cos(angle), sin(angle))
                chars.append(chi)
            return chars
        # General 2^k: skip for now
        return [{a: 1 if gcd(a, pk) == 1 else 0 for a in range(pk)}]

    # Build dlog table
    dlog = {}
    val = 1
    for k in range(phi):
        dlog[val] = k
        val = val * g % pk

    chars = []
    for j in range(phi):
        chi = {}
        for a in range(pk):
            if gcd(a, pk) != 1:
                chi[a] = 0
            else:
                k = dlog[a]
                angle = 2 * pi * j * k / phi
                chi[a] = complex(cos(angle), sin(angle))
        chars.append(chi)
    return chars

def main():
    b = 10
    m = b * b

    # Build S table
    S_table = {}
    for p in range(m + 1, 50000):
        if not is_prime(p) or gcd(p, b) != 1:
            continue
        a = p % m
        if a in S_table:
            continue
        C = sum(1 for r in range(1, p)
                if b * r // p == b * (b * r % p) // p)
        S_table[a] = C - (p - 1) // b

    units = [a for a in range(1, m) if gcd(a, m) == 1]
    phi_m = len(units)

    # Class means for centering
    cls = defaultdict(list)
    for a in units:
        if a in S_table:
            cls[(a - 1) % b].append(S_table[a])
    mean_R = {R: sum(v)/len(v) for R, v in cls.items()}

    S_cent = {}
    for a in units:
        if a in S_table:
            S_cent[a] = S_table[a] - mean_R[(a - 1) % b]

    # Build characters
    all_chars = build_characters_mod_n(m)
    print(f"Base {b}, m={m}, phi={phi_m}, chars={len(all_chars)}")

    # Classify each character
    char_info = []
    for idx, chi in enumerate(all_chars):
        # Check if odd: chi(m-1) = chi(-1) = -1
        minus1 = m - 1
        val_minus1 = chi.get(minus1, 0)
        is_odd = abs(val_minus1 - (-1)) < 1e-6
        is_trivial = all(abs(chi.get(a, 0) - 1) < 1e-6
                        for a in units)

        # Compute s_hat
        s_hat = sum(S_cent.get(a, 0) * (chi.get(a, 0).conjugate()
                    if isinstance(chi.get(a, 0), complex)
                    else chi.get(a, 0))
                    for a in units) / phi_m

        char_info.append({
            'idx': idx,
            'chi': chi,
            'is_odd': is_odd,
            'is_trivial': is_trivial,
            's_hat': complex(s_hat)
        })

    odd_chars = [c for c in char_info if c['is_odd']]
    print(f"Odd characters: {len(odd_chars)}")
    print()

    # Print s_hat for odd characters
    print("Collision transform coefficients (odd characters):")
    print(f"{'idx':>4} {'|s_hat|':>8} {'arg/pi':>8}")
    for c in odd_chars:
        sh = c['s_hat']
        print(f"{c['idx']:>4} {abs(sh):>8.4f} {atan2(sh.imag, sh.real)/pi:>+8.4f}")
    print()

    # Compute P(s, chi) for each odd character
    s_values = [1.0, 0.8, 0.6, 0.5]
    for c in odd_chars:
        c['P'] = {s: complex(0) for s in s_values}

    count = 0
    for p in range(m + 1, 5000000):
        if not is_prime(p) or gcd(p, b) != 1:
            continue
        count += 1
        a = p % m
        for c in odd_chars:
            chi_p = c['chi'].get(a, 0)
            if chi_p == 0: continue
            for s in s_values:
                c['P'][s] += chi_p * p ** (-s)

    print(f"Primes: {count}")
    print()

    for s in s_values:
        print(f"=== s = {s} ===")
        print(f"{'idx':>4} {'Re(sh*P)':>12} {'Im(sh*P)':>12} {'|sh*P|':>10} {'sign':>5}")

        pos_count = 0
        neg_count = 0
        total_re = 0

        for c in odd_chars:
            sh = c['s_hat']
            P = c['P'][s]
            prod = sh * P
            sign = '+' if prod.real >= -1e-10 else '-'
            if prod.real >= -1e-10:
                pos_count += 1
            else:
                neg_count += 1
            total_re += prod.real

            print(f"{c['idx']:>4} {prod.real:>+12.6f} {prod.imag:>+12.6f} "
                  f"{abs(prod):>10.6f} {sign:>5}")

        print(f"Positive: {pos_count}, Negative: {neg_count}, "
              f"Sum Re: {total_re:+.6f}")
        print()

if __name__ == '__main__':
    main()
